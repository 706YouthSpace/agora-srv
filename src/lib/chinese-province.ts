    if (code.endsWith('0000')) {
        provinces[name] = code;
        if (!provinceToCities[name]) {
            provinceToCities[name] = {}
        }
    } else if (code.endsWith('00')) {
        if (name) {
            cities[name] = code;
            const province = (DISTRICTS as any)[`${(code as string).substring(0, 2)}0000`];
            if (province) {
                const cityMap = provinceToCities[province[0]];
                if (cityMap) {
                    cityMap[name] = code;
                }
            }
        }
    } else {
        if (name) {
            const current = countys[name];
            if (current && typeof current === 'string') {
                countys[name] = [current, code];
            } else if (Array.isArray(current)) {
                current.push(code)
            } else {
                countys[name] = code;
            }
        }
    }
